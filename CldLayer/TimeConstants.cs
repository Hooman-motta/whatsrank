namespace CldLayer {
    /// <summary>
    /// Time Constants
    /// </summary>
    public static class TimeConstants {
        /// <summary>
        /// Second = 1
        /// </summary>
        public const int Second = 1;
        /// <summary>
        /// Minute = 60 * Second
        /// </summary>
        public const int Minute = 60 * Second;
        /// <summary>
        /// Hour = 60 * Minute
        /// </summary>
        public const int Hour = 60 * Minute;
        /// <summary>
        /// Day = 24 * Hour
        /// </summary>
        public const int Day = 24 * Hour;
        /// <summary>
        /// Month = 30 * Day
        /// </summary>
        public const int Month = 30 * Day;
    }
}